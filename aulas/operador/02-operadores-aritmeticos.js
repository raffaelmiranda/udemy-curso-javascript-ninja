console.log("OPERADORES DE INCREMENTO e DECREMENTO");
var pos_incremento = 50;
console.log(pos_incremento++); //50
console.log(pos_incremento); //51

var pos_decremento = 60;
console.log(pos_decremento--); //60
console.log(pos_decremento); //59

var pre_incremento = 70;
console.log(++pre_incremento); //71

var pre_decremento = 80;
console.log(--pre_decremento); //79
